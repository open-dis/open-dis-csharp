using System;
using System.Diagnostics;
using System.Diagnostics.CodeAnalysis;
using System.Globalization;
using System.Text;
using System.Xml.Serialization;
using OpenDis.Core;

namespace OpenDis.Dis1998
{
    /// <summary>
    /// Section 5.2.33. Three floating point values, x, y, and z
    /// </summary>
    [Serializable]
    [XmlRoot]
    public partial class Vector3Float : IEquatable<Vector3Float>, IReflectable
    {
        /// <summary>
        /// X value
        /// </summary>
        private float _x;

        /// <summary>
        /// y Value
        /// </summary>
        private float _y;

        /// <summary>
        /// Z value
        /// </summary>
        private float _z;

        /// <summary>
        /// Initializes a new instance of the <see cref="Vector3Float"/> class.
        /// </summary>
        public Vector3Float()
        {
        }

        /// <summary>
        /// Implements the operator !=.
        /// </summary>
        /// <param name="left">The left operand.</param>
        /// <param name="right">The right operand.</param>
        /// <returns>
        ///    <c>true</c> if operands are not equal; otherwise, <c>false</c>.
        /// </returns>
        public static bool operator !=(Vector3Float left, Vector3Float right) => !(left == right);

        /// <summary>
        /// Implements the operator ==.
        /// </summary>
        /// <param name="left">The left operand.</param>
        /// <param name="right">The right operand.</param>
        /// <returns>
        ///    <c>true</c> if both operands are equal; otherwise, <c>false</c>.
        /// </returns>
        public static bool operator ==(Vector3Float left, Vector3Float right)
            => ReferenceEquals(left, right) || (left is not null && right is not null && left.Equals(right));

        public virtual int GetMarshalledSize()
        {
            int marshalSize = 0;

            marshalSize += 4;  // this._x
            marshalSize += 4;  // this._y
            marshalSize += 4;  // this._z
            return marshalSize;
        }

        /// <summary>
        /// Gets or sets the X value
        /// </summary>
        [XmlElement(Type = typeof(float), ElementName = "x")]
        public float X
        {
            get => _x;

            set => _x = value;
        }

        /// <summary>
        /// Gets or sets the y Value
        /// </summary>
        [XmlElement(Type = typeof(float), ElementName = "y")]
        public float Y
        {
            get => _y;

            set => _y = value;
        }

        /// <summary>
        /// Gets or sets the Z value
        /// </summary>
        [XmlElement(Type = typeof(float), ElementName = "z")]
        public float Z
        {
            get => _z;

            set => _z = value;
        }

        /// <summary>
        /// Occurs when exception when processing PDU is caught.
        /// </summary>
        public event EventHandler<PduExceptionEventArgs> ExceptionOccured;

        /// <summary>
        /// Called when exception occurs (raises the <see cref="Exception"/> event).
        /// </summary>
        /// <param name="e">The exception.</param>
        protected void RaiseExceptionOccured(Exception e)
        {
            if (PduBase.FireExceptionEvents && ExceptionOccured != null)
            {
                ExceptionOccured(this, new PduExceptionEventArgs(e));
            }
        }

        /// <summary>
        /// Marshal the data to the DataOutputStream. Note: Length needs to be set before calling this method
        /// </summary>
        /// <param name="dos">The DataOutputStream instance to which the PDU is marshaled.</param>
        [SuppressMessage("Microsoft.Design", "CA1031:DoNotCatchGeneralExceptionTypes", Justification = "Due to ignoring errors.")]
        public virtual void Marshal(DataOutputStream dos)
        {
            if (dos != null)
            {
                try
                {
                    dos.WriteFloat(_x);
                    dos.WriteFloat(_y);
                    dos.WriteFloat(_z);
                }
                catch (Exception e)
                {
                    if (PduBase.TraceExceptions)
                    {
                        Trace.WriteLine(e);
                        Trace.Flush();
                    }

                    RaiseExceptionOccured(e);

                    if (PduBase.ThrowExceptions)
                    {
                        throw;
                    }
                }
            }
        }

        [SuppressMessage("Microsoft.Design", "CA1031:DoNotCatchGeneralExceptionTypes", Justification = "Due to ignoring errors.")]
        public virtual void Unmarshal(DataInputStream dis)
        {
            if (dis != null)
            {
                try
                {
                    _x = dis.ReadFloat();
                    _y = dis.ReadFloat();
                    _z = dis.ReadFloat();
                }
                catch (Exception e)
                {
                    if (PduBase.TraceExceptions)
                    {
                        Trace.WriteLine(e);
                        Trace.Flush();
                    }

                    RaiseExceptionOccured(e);

                    if (PduBase.ThrowExceptions)
                    {
                        throw;
                    }
                }
            }
        }

        ///<inheritdoc/>
        [SuppressMessage("Microsoft.Design", "CA1031:DoNotCatchGeneralExceptionTypes", Justification = "Due to ignoring errors.")]
        public virtual void Reflection(StringBuilder sb)
        {
            sb.AppendLine("<Vector3Float>");
            try
            {
                sb.AppendLine("<x type=\"float\">" + _x.ToString(CultureInfo.InvariantCulture) + "</x>");
                sb.AppendLine("<y type=\"float\">" + _y.ToString(CultureInfo.InvariantCulture) + "</y>");
                sb.AppendLine("<z type=\"float\">" + _z.ToString(CultureInfo.InvariantCulture) + "</z>");
                sb.AppendLine("</Vector3Float>");
            }
            catch (Exception e)
            {
                if (PduBase.TraceExceptions)
                {
                    Trace.WriteLine(e);
                    Trace.Flush();
                }

                RaiseExceptionOccured(e);

                if (PduBase.ThrowExceptions)
                {
                    throw;
                }
            }
        }

        /// <inheritdoc/>
        public override bool Equals(object obj) => this == obj as Vector3Float;

        ///<inheritdoc/>
        public bool Equals(Vector3Float obj)
        {
            bool ivarsEqual = true;

            if (obj.GetType() != GetType())
            {
                return false;
            }

            if (_x != obj._x)
            {
                ivarsEqual = false;
            }

            if (_y != obj._y)
            {
                ivarsEqual = false;
            }

            if (_z != obj._z)
            {
                ivarsEqual = false;
            }

            return ivarsEqual;
        }

        /// <summary>
        /// HashCode Helper
        /// </summary>
        /// <param name="hash">The hash value.</param>
        /// <returns>The new hash value.</returns>
        private static int GenerateHash(int hash) => hash << (5 + hash);

        /// <inheritdoc/>
        public override int GetHashCode()
        {
            int result = 0;

            result = GenerateHash(result) ^ _x.GetHashCode();
            result = GenerateHash(result) ^ _y.GetHashCode();
            result = GenerateHash(result) ^ _z.GetHashCode();

            return result;
        }
    }
}
